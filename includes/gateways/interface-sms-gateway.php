/**
 * اینترفیس درگاه پیامکی.
 */
interface WC_SMS_Gateway_Interface {

	/**
	 * ارسال پیامک کد یکبار مصرف (OTP) از طریق درگاه پیامکی فعال.
	 *
	 * @param string $phone      شماره موبایل مقصد (فرمت استاندارد ایران، مانند 09xxxxxxxxx).
	 * @param string $code       کد یکبار مصرف تولید شده جهت ارسال.
	 * @param string $pattern_id شناسه/کد پترن پیامک تاییدشده در پنل درگاه (در صورت پشتیبانی درگاه).
	 *
	 * @return bool|WP_Error نتیجه true در صورت ارسال موفق، یا WP_Error در صورت بروز خطا.
	 */
	public function send_otp( $phone, $code, $pattern_id );
}
